<h3 style="width:100%;text-align:center;">上海房屋租赁合同</h3>
<p style="width:100%;font-size:18px;">出租方（以下简称甲方）<u>　　　　　　　　　　　　　　　　</u></p>
<p style="width:100%;font-size:18px;">承租方（以下简称乙方）<u>　　　　　　　　　　　　　　　　</u></p>
<p style="width:100%;font-size:18px;text-indent:40px;margin-top:40px;">根据《中华人民共和国合同法》及相关法律法规的规定，甲、乙双方在平等、自愿的基础上，就甲方将房屋出租给乙方使用，乙方承租甲方房屋事宜，为明确双方权利义务，经协商一致，订立本合同。</p>
<p class="text">第一条甲方保证所出租的房屋符合国家对租赁房屋的有关规定。</p>
<p class="text">第二条房屋的坐落、面积、装修、设施情况</p>
<p class="text">1、甲方出租给乙方的房屋位于<u>　　　　</u>市<u>　　　　</u>（区/县）<u>　　　　　　　</u>；具体地址为<u>　　　　　　　　　　　　　　　　　　　　　　　　　　　　</u></p>
<p class="text">2、出租房屋面积共<u>　　　　　　　</u>平方米（建筑面积/使用面积/套内面积）</p>
<p class="text">3、该房屋现有装修及设施、设备情况详见合同附件。</p>
<p class="text">该附件作为甲方按照本合同约定交付乙方使用和乙方在本合同租赁期满交还该房屋时的验收依据。</p>
<p class="text">第三条甲方应提供房产证（或具有出租权的有效证明）身份证明（营业执照）等文件，乙方应提供身份证明文件。双方验证后可复印对方文件备存。所有复印件仅供本次租赁使用。</p>
<p class="text">第四条租赁期限、用途</p>
<p class="text">1、该房屋租赁期共<u>　　</u>个月。自<u>　　</u>年<u>　　</u>月<u>　　</u>日起至<u>　　</u>年<u>　　</u>月<u>　　</u>日止</p>
<p class="text">2、租赁期满，甲方有权收回出租房屋，乙方应如期交还。</p>
<p class="text">第五条租金及支付方式</p>
<p class="text">该房屋每月租金为人民币<u>　　　　　　</u>元整。</p>
<p class="text">第六条租赁期间相关费用及税金</p>
<p class="text">1．甲方应承担的费用：</p>
<p class="text">租赁期间，房屋和土地的产权税由甲方依法交纳。如果发生政府有关部门征收本合同中未列出项目但与该房屋有关的费用，应由甲方负担。</p>
<p class="text"><pstyle="width:100%;font-size:18px;text-indent:40px;line-height:30px;">2．乙方应按时交纳自行负担的费用。</p>
<p class="text">甲方不得擅自增加本合同未明确由乙方交纳的费用。</p>
<p class="text">第七条房屋修缮与使用</p>
<p class="text">1、在租赁期内，甲方应保证出租房屋的使用安全。该房屋及所属设施的维>修责任除双方在本合同及补充条款中约定外，均由甲方负责（乙方使用不当造成的损坏除外）</p>
<p class="text">甲方提出进行维修须提前<u>　　</u>日书面通知乙方，乙方应积极协助配合。</p>
<p class="text">乙方向甲方提出维修请求后，甲方应及时提供维修服务。</p>
<p class="text">对乙方在甲方允许情况下装修装饰部分甲方不负有修缮的义务。</p>
<p class="text">2、乙方应合理使用其所承租的房屋及其附属设施。如因使用不当造成房屋及设施损坏的，乙方应立即负责修复或赔偿损失。</p>
<p class="text">乙方如改变房屋的内部结构、装修或设置对房屋结构有影响的设备，设计规模、范围、工艺、用料等方案均须事先征得甲方的书面同意后方可施工。租赁期满后或因乙方责任导致退租的，除双方另有约定外，甲方有权选择以下权利中的一种：</p>
<p class="text">（1）依附于房屋的装修归甲方所有。</p>
<p class="text">（2）要求乙方恢复原状。</p>
<p class="text">（3）向乙方收取恢复工程实际发生的费用。</p>
<p class="text">第八条房屋的转让与转租</p>
<p class="text">1、租赁期间，甲方有权依照法定程序向第三方转让该出租的房屋，转让后，</p>
<p class="text">本合同对新的房屋所有权人和乙方继续有效。</p>
<p class="text">2、未经甲方同意，乙方不得转租、转借承租房屋。</p>
<p class="text">3、甲方出售房屋，须在1个月前书面通知乙方，在同等条件下，乙方有优先购买权。</p>
<p class="text">第九条合同的变更、解除与终止</p>
<p class="text">1、双方可以协商变更或终止本合同。</p>
<p class="text">2、甲方有以下行为之一的，乙方有权解除合同：</p>
<p class="text">（1）不能提供房屋或所提供房屋不符合约定条件，严重影响居住。</p>
<p class="text">（2）甲方未尽房屋修缮义务，严重影响居住的。</p>
<p class="text">3、房屋租赁期间，乙方有下列行为之一的，甲方有权解除合同，收回出租房屋；</p>
<p class="text">（1）未经甲方书面同意，转租、转借承租房屋。</p>
<p class="text">（2）未经甲方书面同意，拆改变动房屋结构。</p>
<p class="text">（3）损坏承租房屋，在甲方提出的合理期限内仍未修复的。</p>
<p class="text">（4）未经甲方书面同意，改变本合同约定的房屋租赁用途。</p>
<p class="text">（5）利用承租房屋存放危险物品或进行违法活动。</p>
<p class="text">（6）逾期未交纳按约定应当由乙方交纳的各项费用，已经给甲方造成严重损害的。</p>
<p class="text">（7）拖欠房租累计3个月以上。</p>
<p class="text">4、租赁期满前，乙方要继续租赁的，应当在租赁期满1个月前书面通知甲方。如甲方在租期届满后仍要对外出租的，在同等条件下，乙方享有优先承租权。</p>
<p class="text">5、租赁期满后合同自然终止。</p>
<p class="text">6、因不可抗力因素导致合同无法履行的，合同终止。</p>
<p class="text">第十条房屋交付及收回的验收</p>
<p class="text">1、甲方应保证租赁房屋本身及附属设施、设备处于能够正常使用状态。</p>
<p class="text">2、验收时双方共同参与，如对装修、器物等硬件设施、设备有异议应当场提出。当场难以检测判断的，应于<u>　　　　</u>日内向对方主张。</p>
<p class="text">3、乙方应于房屋租赁期满后，将承租房屋及附属设施、设备交还甲方。</p>
<p class="text">4、乙方交还甲方房屋应当保持房屋及设施、设备的完好状态，不得留存物品或影响房屋的正常使用。对未经同意留存的物品，经甲方催告后乙方仍不取回的甲方有权处置。</p>
<p class="text">第十一条甲方违约责任处理规定</p>
<p class="text">1、甲方因不能提供本合同约定的房屋而解除合同的，应支付乙方本合同租金总额<u>　　　　</u>%的违约金。甲方除应按约定支付违约金外，还应对超出违约金以外的损失进行赔偿。</p>
<p class="text">2、如乙方要求甲方继续履行合同的，甲方每逾期交房一日，则每日应向乙方支付日租金<u>　　　　</u>倍的滞纳金。甲方还应承担因逾期交付给乙方造成的损失。</p>
<p class="text">3、由于甲方怠于履行维修义务或情况紧急，乙方组织维修的，甲方应支付乙方费用或折抵租金，但乙方应提供有效凭证。</p>
<p class="text">4、甲方违反本合同约定，提前收回房屋的，应按照合同总租金的<u>　　　　</u>%向乙方支付违约金，若支付的违约金不足弥补乙方损失的，甲方还应该承担赔偿责任。</p>
<p class="text">5、甲方因房屋权属瑕疵或非法出租房屋而导致本合同无效时，甲方应赔偿乙方损失。</p>
<p class="text">第十二条乙方违约责任</p>
<p class="text">（1）未经甲方书面同意，拆改变动房屋结构或损坏房屋；</p>
<p class="text">（2）改变本合同规定的租赁用途或利用该房屋进行违法活动的；</p>
<p class="text">（3）拖欠房租累计3个月以上的。</p>
<p class="text">2、在租赁期内，乙方逾期交纳本合同约定应由乙方负担的费用的，每逾期一天，则应按上述费用总额的<u>　　　　</u>%支付甲方滞纳金。</p>
<p class="text">第十三条免责条件</p>
<p class="text">1、因不可抗力原因致使本合同不能继续履行或造成的损失，甲、乙双方互不承担责任。</p>
<p class="text">2、因国家政策需要拆除或改造已租赁的房屋，使甲、乙双方造成损失的，互不承担责任。</p>
<p class="text">3、因上述原因而终止合同的，租金按照实际使用时间计算，不足整月的按天数计算，多退少补。</p>
<p class="text">4、不可抗力系指“不能预见、不能避免并不能克服的客观情况”。第十四条本合同未尽事宜，经甲、乙双方协商一致，可订立补充条款。补充条款及附件均为本合同组成部分，与本合同具有同等法律效力。</p>
<p class="text">第十五条争议解决</p>
<p class="text">本合同项下发生的争议，由双方当事人协商或申请调解；协商或调解解决不成的，按下列第2种方式解决（以下两种方式只能选择一种）</p>
<p class="text">1、提请仲裁委员会仲裁。</p>
<p class="text">2、依法向有管辖权的人民法院提起诉讼。</p>
